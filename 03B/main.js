let result = document.querySelector(".result")

const getTreesHitCount = (terrain, rightAmt, downAmt) => {
  let hitCount = 0;
  let terrainWidth = terrain[0].length
  let currentYPos = 0
  let currentXPos = 0
  while (currentYPos < terrain.length-downAmt) {
    currentYPos += downAmt;
    currentXPos += rightAmt;
    currentXPos %= terrainWidth;
    if (terrain[currentYPos][currentXPos] == "#")
      hitCount++
  }
  return hitCount
}

let slope1 = getTreesHitCount(puzzle_terrain, 1, 1)
let slope2 = getTreesHitCount(puzzle_terrain, 3, 1)
let slope3 = getTreesHitCount(puzzle_terrain, 5, 1)
let slope4 = getTreesHitCount(puzzle_terrain, 7, 1)
let slope5 = getTreesHitCount(puzzle_terrain, 1, 2)

result.innerHTML = "2020 Question 3B - Trees Hit => " 
    + "(1,1) = " + slope1
    + ", (3,1) = " + slope2
    + ", (5,1) = " + slope3
    + ", (7,1) = " + slope4
    + ", (1,2) = " + slope5
    + ". Hence answer is: " + (slope1*slope2*slope3*slope4*slope5)